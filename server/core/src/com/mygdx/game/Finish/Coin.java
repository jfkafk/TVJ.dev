package com.mygdx.game.Finish;

public class Coin {

    float xCoordinate;
    float yCoordinate;

    public Coin(float x, float y) {
        xCoordinate = x;
        yCoordinate = y;
    }

    public float getxCoordinate() {
        return xCoordinate;
    }

    public float getyCoordinate() {
        return yCoordinate;
    }
}
